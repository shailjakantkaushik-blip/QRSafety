  * {
    @apply border-border;
  }

  body {
    @apply bg-background text-foreground;
  }
}
CSS

# 2) PostCSS config (Tailwind compile)
if [ -f postcss.config.mjs ]; then
  cat > postcss.config.mjs <<'POSTCSS'
const config = {
  plugins: {
    tailwindcss: {},
    autoprefixer: {},
  },
};
export default config;
POSTCSS
else
  cat > postcss.config.js <<'POSTCSS'
module.exports = {
  plugins: {
    tailwindcss: {},
    autoprefixer: {},
  },
};
POSTCSS
fi

# 3) Tailwind config: make bg-background etc exist
if [ -f tailwind.config.ts ]; then
  cat > tailwind.config.ts <<'TW'
